from PyQt5.QtWidgets import QDialog, QVBoxLayout, QTextEdit, QPushButton
from PyQt5.QtCore import Qt

def show_help():
    """
    Отображает окно справки с инструкциями по использованию программы.

    Создает диалоговое окно с текстом помощи, используя QTextEdit.
    """
    # Создание диалогового окна
    dialog = QDialog()
    dialog.setWindowTitle("Справка по программе")
    dialog.setMinimumSize(500, 400)

    # Основной макет
    layout = QVBoxLayout()

    # Текстовое поле для инструкций
    help_text = QTextEdit()
    help_text.setReadOnly(True)
    help_text.setText("""
    <h2>Справка по использованию программы</h2>
    <p>Эта программа предназначена для синтаксического анализа текста на русском языке из RTF-файлов.</p>
    <h3>Инструкции по использованию:</h3>
    <ul>
        <li><b>Загрузка файла:</b> Нажмите кнопку "Загрузить RTF" и выберите файл в формате RTF. Текст будет отображен в текстовом поле.</li>
        <li><b>Анализ текста:</b> Нажмите кнопку "Анализировать" для выполнения синтаксического анализа. Результаты появятся в виде дерева зависимостей.</li>
        <li><b>Просмотр результатов:</b> Дерево показывает ID, слово, часть речи, тип связи и родительский узел для каждого токена.</li>
        <li><b>Сохранение результатов:</b> Нажмите "Сохранить результаты" для экспорта в JSON-файл.</li>
        <li><b>Редактирование:</b> Измените текст в текстовом поле и повторно нажмите "Анализировать" для обновления результатов.</li>
        <li><b>Документирование:</b> Результаты можно экспортировать в текстовый формат для отчета через сохранение.</li>
    </ul>
    <h3>Советы:</h3>
    <ul>
        <li>Убедитесь, что RTF-файл содержит текст на русском языке.</li>
        <li>При возникновении ошибок (например, неверный формат файла), программа выдаст сообщение.</li>
        <li>Для больших текстов анализ может занять несколько секунд.</li>
    </ul>
    """)
    help_text.setStyleSheet("QTextEdit { font-size: 14px; padding: 10px; }")
    layout.addWidget(help_text)

    # Кнопка закрытия
    close_button = QPushButton("Закрыть")
    close_button.clicked.connect(dialog.accept)
    close_button.setStyleSheet("QPushButton { padding: 5px; }")
    layout.addWidget(close_button, alignment=Qt.AlignRight)

    # Установка макета и отображение окна
    dialog.setLayout(layout)
    dialog.exec_()