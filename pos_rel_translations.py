def translate_pos(pos):
    """
    Переводит теги частей речи Natasha в читаемые названия.
    """
    pos_map = {
        'NOUN': 'существительное',
        'VERB': 'глагол',
        'ADJ': 'прилагательное',
        'ADV': 'наречие',
        'PRON': 'местоимение',
        'PREP': 'предлог',
        'CONJ': 'союз',
        'PRCL': 'частица',
        'NUM': 'числительное',
        'GRND': 'деепричастие',
        None: 'пунктуация'
    }
    return pos_map.get(pos, 'неизвестно')

